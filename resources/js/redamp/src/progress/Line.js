Ext.define('RedAmp.progress.Line', {
	extend:'Ext.Component',
	
	///////////////////////////////////////////////////////////////////////////
	// Config
	///////////////////////////////////////////////////////////////////////////
	config:{
	},
	
	
	///////////////////////////////////////////////////////////////////////////
	// Constructor
	///////////////////////////////////////////////////////////////////////////
	constructor: function(config){
		this.initConfig(config);
		this.callParent(arguments);
	},
	
	///////////////////////////////////////////////////////////////////////////
	// Render Template
	///////////////////////////////////////////////////////////////////////////
	renderTpl:[
        '<div id="{id}-progressLineEl" class="progress-line" style="width: 0px;"></div>'
	],
	
	///////////////////////////////////////////////////////////////////////////
	// Child Elements
	///////////////////////////////////////////////////////////////////////////
	childEls: [
		'progressLineEl'
    ],
	
	///////////////////////////////////////////////////////////////////////////
	// Render Events
	///////////////////////////////////////////////////////////////////////////
	beforeRender: function () {
		//Call parent method
        this.callParent();

        //Set the renderData
		Ext.applyIf(this.renderData, {
		});
    },
	
	onRender: function(){
		this.callParent(arguments);
	},
	
	///////////////////////////////////////////////////////////////////////////
	// Methods
	///////////////////////////////////////////////////////////////////////////
	updateProgress: function(progress){
		this.progressLineEl.setWidth((progress * 100) + "%");
	}
});